const btn3 = document.getElementById("btn3");
const scoreDisplay = document.getElementById("score");
const scoreValue = document.querySelector("#score span");
const quizNote = document.querySelector(".quizCard h3");
const retakeBtns = document.querySelector(".quizCard .buttons");
const retakeBtn = document.getElementById("retake");
const homeBtn = document.getElementById("home");
const themeBtn = document.getElementById("themeBtn");

// Variables
var score = 0;
var questionSet;
var currentQuestion = 0;
var question;
var selectedAnswer;
var questionTypeAnimation;

// Get the current date
const todaysDate = new Date();
// Define the target date
const targetDate = new Date("April 30, 2023");
// Calculate the difference in time between the target date and today's date
const timeDiff = targetDate.getTime() - todaysDate.getTime();
// Convert the time difference to days
const remainingDays = Math.ceil(timeDiff / (1000 * 3600 * 24));
title.textContent = remainingDays + " days...";

loadTopics();

function loadTopics() {
  questions.forEach((question) => {
    const topic = document.createElement("div");
    topic.classList.add("topic");
    topic.innerHTML = `<p>${question.subject}<br>${question.topic}</p>
                      <div>
                        <a href="${
                          question.note
                        }" target="_blank"><i class="fa-solid fa-book-open"></i>Note</a>
                        <button onclick="chooseQuestionSet(${questions.indexOf(
                          question
                        )})" value="${questions.indexOf(
      question
    )}"><i class="fa-solid fa-clipboard"></i>Test</button>
                      </div>`;
    topics.appendChild(topic);
  });
}

function chooseQuestionSet(setIndex) {
  questionSet = questions[setIndex];
  if (questionSet.content.length < 1) {
    quizNote.classList.add("active");
    retakeBtns.classList.add("active");
    retakeBtn.style.display = "none";
    quizCard.style.display = "flex";
    main.classList.add("height");
    topics.style.display = "none";
    scoreDisplay.style.display = "none";
    document.body.classList.add("retake");
  } else {
    main.classList.add("active");
    scoreValue.textContent = score + "/" + questionSet.content.length;
    setTimeout(() => {
      quizCard.style.display = "flex";
      main.classList.add("height");
      displayQuestion();
    }, 800);
    setTimeout(() => {
      main.classList.remove("active");
    }, 1400);
  }
}

function displayQuestion() {
  title.textContent = questionSet.subject + " | " + questionSet.topic;
  topics.style.display = "none";
  if (currentQuestion < questionSet.content.length) {
    question = questionSet.content[currentQuestion];
    console.log(question);
    questionDisplay.innerHTML = "";

    if (questionTypeAnimation) {
      clearInterval(questionTypeAnimation);
    }

    let index = 0;
    questionTypeAnimation = setInterval(() => {
      if (index < question.question.length) {
        questionDisplay.innerHTML += question.question.charAt(index);
        index++;
      } else {
        options.classList.add("visible");
        clearInterval(questionTypeAnimation);
      }
    }, 10);

    btn0.innerHTML = question.options[0];
    btn1.innerHTML = question.options[1];
    btn2.innerHTML = question.options[2];
    btn3.innerHTML = question.options[3];
  } else {
    retakeScreen();
  }
}

btn0.addEventListener("click", () => {
  checkAnswer(0);
});

btn1.addEventListener("click", () => {
  checkAnswer(1);
});

btn2.addEventListener("click", () => {
  checkAnswer(2);
});

btn3.addEventListener("click", () => {
  checkAnswer(3);
});

function checkAnswer(selectedAnswer) {
  options.classList.add("disable");
  if (selectedAnswer == question.answer) {
    score++;
    scoreValue.textContent = score + "/" + questionSet.content.length;
    document.getElementById(`btn${selectedAnswer}`).classList.add("correct");
    currentQuestion++;
    setTimeout(() => {
      nextQuestion();
    }, 1000);
  } else {
    currentQuestion++;
    document.getElementById(`btn${selectedAnswer}`).classList.add("incorrect");
    document.getElementById(`btn${question.answer}`).classList.add("correct");
    setTimeout(() => {
      nextQuestion();
    }, 1000);
  }
}

function nextQuestion() {
  options.classList.remove("disable");
  options.classList.remove("visible");
  buttons.forEach((button) => {
    button.classList.remove("correct", "incorrect");
  });
  setTimeout(() => {
    questionDisplay.textContent = "";
    displayQuestion();
  }, 500);
}

function retakeScreen() {
  retakeBtns.classList.add("active");
  scoreDisplay.classList.add("active");
  document.body.classList.add("retake");
}

retakeBtn.addEventListener("click", () => {
  document.body.classList.remove("retake");
  retakeBtns.classList.remove("active");
  scoreDisplay.classList.remove("active");
  score = 0;
  scoreValue.textContent = score + "/" + questionSet.content.length;
  currentQuestion = 0;
  displayQuestion();
});

homeBtn.addEventListener("click", () => {
  goToHome();
});

function goToHome() {
  main.classList.add("active");
  title.textContent = "22 days...";
  setTimeout(() => {
    currentQuestion = 0;
    document.body.classList.remove("retake");
    retakeBtn.style.display = "block";
    quizNote.classList.remove("active");
    scoreDisplay.style.display = "block";
    retakeBtns.classList.remove("active");
    questionDisplay.innerHTML = "";
    options.classList.remove("disable");
    options.classList.remove("visible");
    scoreDisplay.classList.remove("active");
    quizCard.style.display = "none";
    main.classList.remove("height");
    topics.style.display = "grid";
    score = 0;
    scoreValue.textContent = score + "/" + questionSet.content.length;
  }, 800);
  setTimeout(() => {
    main.classList.remove("active");
  }, 1400);
}

themeBtn.addEventListener("click", () => {
  const body = document.body;
  body.classList.toggle("light");
  body.classList.toggle("dark");
  themeBtn.classList.toggle("active");
});
